const Loading = ({ calculatedWidth }) => (
  <aside>
    <div className="loading-bar">
      <label htmlFor="images-loaded">Loading all your favorite images...</label>
      <progress id="images-loaded" max="100" value={calculatedWidth}></progress>
    </div>
  </aside>
);

const App = () => {
  const [currentImage, setCurrentImage] = useState(0);
  const [numLoaded, setNumLoaded] = useState(0);

  let { data, loading, error } = usePalette(images[currentImage]);

  const handleClick = () => {
    const length = images.length - 1;

    setCurrentImage(
      (currentImage) => (currentImage < length ? currentImage + 1 : 0)
      //{ data, loading, error } = usePalette(images[0]);
    );
  };

  const handleImageLoad = () => {
    setNumLoaded((numLoaded) => numLoaded + 1);
  };

  const Currentstyle = {
    color: data.darkVibrant,
    backgroundColor: data.lightMuted
  };

  return (
    <section style={Currentstyle}>
      <header>
        <h1>Contrast</h1>
        <h2>
          A little react.js project <br /> by Dimitri Leduc
        </h2>
      </header>

      <figure>
        {numLoaded < images.length && (
          <Loading calculatedWidth={(numLoaded / images.length) * 100} />
        )}
        <figcaption>
          {currentImage + 1} / {images.length}
        </figcaption>
        {images.map((imageURL, index) => (
          <img
            alt=""
            key={imageURL}
            src={imageURL}
            onClick={handleClick}
            onLoad={handleImageLoad}
            className={currentImage === index ? "display" : "hide"}
          />
        ))}
      </figure>
    </section>
  );
};

export default App;
